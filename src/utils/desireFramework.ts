export const AD_STRUCTURE = `
AD CREATIVE FRAMEWORK:

SYSTEM 1 (Emotion) + SYSTEM 2 (Logic) = CONVERSION
- System 1: The HOOK. Triggers an emotional response in <2 seconds. Gets the click/watch.
- System 2: The LOGIC. Builds belief through mechanism + proof. Gets the purchase.

IMAGE AD: Job = GET THE CLICK (not sell the product)
- Must trigger curiosity or recognition in the scroll
- Show the DESIRE STATE (outcome) or PAIN STATE (recognition)
- Copy: 5-10 words max. Speak to the desire, not the product.

VIDEO AD STRUCTURE:
1. HOOK (0-3s): Scroll-stopping statement or visual. Address THEM, not the product.
2. PROBLEM (3-8s): Agitate the pain they already feel. Use THEIR language.
3. AGITATE (8-15s): Make the pain worse. Show consequences of inaction.
4. ROOT CAUSE (15-25s): Reveal WHY nothing else worked. The "aha" moment.
5. MECHANISM (25-35s): Show HOW to fix it. The theory of the solution.
6. PRODUCT (35-45s): THIS product delivers the mechanism. Features → Desires.
7. DESIRE (45-55s): Show the transformed life. Paint the dream state.
8. CTA (55-60s): Clear, desire-connected action. "Start your transformation."

LANDING PAGE: Job = EDUCATE → BUILD BELIEF → OVERCOME OBJECTIONS → CONVERT
- Must flow naturally from the ad (ad → LP → checkout = congruent experience)
- Structure mirrors video: Problem → Root Cause → Mechanism → Product → Proof → CTA
`;

