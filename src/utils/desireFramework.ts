/**
 * Desire-Driven Marketing Framework
 * Core methodology integrated throughout the NOMAD pipeline.
 *
 * Key principles:
 *   - People don't buy products — they buy fulfillment of desires
 *   - Desire has a TURNING POINT where pain becomes unbearable → highest conversion
 *   - Amplified desires (loved ones, identity, survival) drive the strongest purchases
 *   - Research uses 4 layers: Avatar → Problem → Solution → Product
 *   - Market sophistication (1-4) determines messaging strategy
 *   - Ads use System 1 (emotion) + System 2 (logic) for conversion
 *   - Root cause + mechanism builds belief, not just product features
 */

// ─────────────────────────────────────────────────────────────
// Desire Intensity & Turning Point
// ─────────────────────────────────────────────────────────────

export const DESIRE_INTENSITY_GUIDE = `
DESIRE INTENSITY SCALE (determines buying probability):
- LOW: Pain is noticeable but manageable. Customer browses, bookmarks, does nothing.
- MODERATE: Pain causes recurring frustration. Customer considers buying but postpones.
- HIGH: Pain reaches TURNING POINT — it's no longer tolerable. Customer buys NOW.
- EXTREME: Survival mode — health, career, relationships threatened. Will pay anything.

THE TURNING POINT is the moment desire becomes unbearable:
- BEFORE turning point: Pain is manageable → customer postpones → low conversion
- AT turning point: Pain becomes unacceptable → customer buys NOW → highest conversion
- AFTER turning point: Survival mode → will pay anything → premium pricing

AMPLIFIED DESIRES (highest-leverage, in order of power):
1. LOVED ONES — especially children. Mothers will sacrifice everything for kids.
   Example: "Clean ingredients" → "Won't harm kids' skin" → "Being a good, protective mother"
2. IDENTITY/STATUS — attractiveness, being a "good" person, masculine/feminine confidence
   Example: "Stop hair loss" → "Look attractive" → "Attract partner, feel confident"
3. SURVIVAL — career, health, independence, financial security
   Example: "Fix back pain" → "Can work again" → "Provide for family"
`;

// ─────────────────────────────────────────────────────────────
// 4-Layer Research Framework
// ─────────────────────────────────────────────────────────────

export const FOUR_LAYER_RESEARCH = `
THE 4 LAYERS OF RESEARCH:

LAYER 1 — AVATAR (Who are they?)
- Demographics: age, gender, income, location, life stage
- Psychographics: values, beliefs, identity, self-image
- Struggles: what keeps them up at night?
- What they've tried before: failed solutions, abandoned products
- Why previous solutions failed: the gap that still exists
- Buzzwords & language: how THEY describe the problem (not how brands describe it)
- Sub-avatar: Go NARROW. "Hair loss sufferers" is too broad.
  "Men 28-35 who noticed thinning in their 20s and want to attract a partner" = much better.

LAYER 2 — PROBLEM (What's causing this?)
- Root cause: What's ACTUALLY happening beneath the symptoms?
- Mechanism: The scientific/logical explanation of WHY the problem exists
- Why it gets worse: What makes the pain compound over time?
- The "aha" explanation: When they understand the root cause, they believe the solution

LAYER 3 — SOLUTION (How does it get fixed?)
- Theory of the solution: HOW to fix it (not the product itself)
- Mechanism of action: The logical path from problem → solution
- Why this approach works when others failed
- Build a chain of YES statements that leads to the inevitable conclusion

LAYER 4 — PRODUCT (Which features deliver which desires?)
- Feature → Benefit → Desire mapping: Every feature connects to a DESIRE
- Proof elements: What evidence proves each feature works?
- Competitive advantage: Why THIS product vs alternatives?
- Price-value perception: Does the price feel justified by the desire intensity?
`;

// ─────────────────────────────────────────────────────────────
// Market Sophistication Levels
// ─────────────────────────────────────────────────────────────

export const MARKET_SOPHISTICATION = `
MARKET SOPHISTICATION LEVELS (determines your entire messaging strategy):

LEVEL 1 — VIRGIN MARKET (Easiest):
  - Audience hasn't heard of this type of solution before
  - Strategy: Simply INTRODUCE the mechanism. "Did you know X causes Y?"
  - Just show it works. No need to differentiate.
  - Example: First collagen supplement in a market that doesn't know about collagen

LEVEL 2 — EARLY COMPETITION:
  - A few competitors exist but no dominant player
  - Strategy: Make BIGGER claims. "Our X works 3x faster"
  - Compete on degree/speed/power
  - Example: "The most bioavailable collagen on the market"

LEVEL 3 — CROWDED MARKET:
  - Many competitors making similar claims
  - Strategy: Introduce a NEW MECHANISM. Don't just say "better" — explain WHY it's better
  - Show the root cause others miss. New angle, new explanation.
  - Example: "Other collagens break down in your stomach. Ours uses micro-encapsulation to reach your skin intact"

LEVEL 4 — SKEPTICAL MARKET (Hardest):
  - Audience has tried everything, believes nothing works
  - Strategy: OVERWHELMING PROOF + personal identification
  - Lead with identity/story, not claims. Show someone JUST LIKE THEM who succeeded.
  - Need: before-afters, testimonials, mechanism proof, money-back guarantees
  - Example: "I tried 14 collagen supplements. Here's the one that actually changed my skin."
`;

// ─────────────────────────────────────────────────────────────
// Root Cause + Mechanism (Belief Building)
// ─────────────────────────────────────────────────────────────

export const ROOT_CAUSE_MECHANISM = `
ROOT CAUSE + MECHANISM FRAMEWORK:

The key to conversion is building BELIEF. You do this by:

1. REVEAL THE ROOT CAUSE
   - Explain WHAT'S ACTUALLY WRONG beneath the symptoms
   - Make them think: "Wait, THAT'S why nothing worked before?"
   - This creates an "aha moment" that reframes the problem

2. PRESENT THE MECHANISM
   - Explain HOW to fix it (the theory of the solution, not the product)
   - Build a logical chain: Problem → Root Cause → How to Fix → Product Does This
   - Make it feel INEVITABLE: "If the root cause is X, then obviously the solution is Y"

3. CHAIN OF YES STATEMENTS
   - Each statement builds on the last:
     "Your skin needs collagen to stay firm, right?" (YES)
     "But after 25, your body produces less collagen every year" (YES)
     "Most collagen supplements break down in your stomach" (YES, I've tried them)
     "So you need a delivery system that protects the collagen until it reaches your skin" (YES, that makes sense)
     "That's exactly what [product] does with [mechanism]" (CONVINCED)

The product should feel like the INEVITABLE conclusion, not a sales pitch.
`;

